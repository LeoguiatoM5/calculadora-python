

#For decrescente
for andar in range(20, 0, -1):
    if andar != 13:
        print(andar)




# Usando o loop 'for' em ordem crescente
for andar in range(1, 21):
    if andar != 13:
        print(andar)


# Usando o loop 'while' em ordem crescente
andar = 1
while andar <= 20:
    if andar != 13:
        print(andar)
    andar += 1



#while o loop em ordem decrescente

andar = 20
while andar >= 1:
    if andar != 13:
        print(andar)
    andar -= 1
